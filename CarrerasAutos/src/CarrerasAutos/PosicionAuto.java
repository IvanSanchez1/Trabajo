package CarrerasAutos;

public class PosicionAuto {
	private int x, y;
	
	public PosicionAuto() {
		this.x = 1;
		this.y = 0;
	}
	
	public int getX() {
		return this.x;
	}
	
	public int getY() {
		return this.y;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
}
